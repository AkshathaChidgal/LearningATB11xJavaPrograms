package ex_01_Java_Basics;

public class Lab012_inc_operator {
    public static void main(String[] args) {

        //pre increment - value is incremented first then it is stored -> ++a
        //post increment - value is printed/stored first then it is incremented -> a++
        int a=10;
        int b=++a; // b=10+1
        System.out.println(a);
        System.out.println(b);
        //Expression & result table
        //line /a| b
        //8 |10 |na
        //9 |11 | 11


    }
}
